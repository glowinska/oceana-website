<h3>How does it work?</h3>
<p>Currently, Oceana has a staff of about 200 and 6,000 volunteers, and it has almost 50 million dollars of revenue (as of 2017). Oceana takes a multi-faceted approach to ocean conservation; It conducts its own scientific research in addition to making policy recommendations, lobbying for specific legislation, and filing and litigating lawsuits.</p>
