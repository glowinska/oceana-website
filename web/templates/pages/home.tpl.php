<h3>What is Oceana?</h3>
<p>Oceana is a nonprofit ocean conservation organization focused on influencing specific policy decisions on the national level to preserve and restore the world's oceans.</p>
<h3>Where is it headquarted?</h3>
<p>It is headquartered in Washington, D.C. with offices in Juneau; Monterey; Fort Lauderdale; New York City; Portland; Toronto, Canada; Mexico City, Mexico; Madrid, Spain; Brussels, Belgium; Copenhagen, Denmark; Geneva, Switzerland; London, England; Manila, Philippines; Belmopan, Belize; Brasilia, Brazil; Santiago, Chile; and Lima, Peru and it is the largest international advocacy group dedicated entirely to ocean conservation.</p>
